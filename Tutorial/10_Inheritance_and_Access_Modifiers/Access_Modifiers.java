
public class Access_Modifiers {
	public static void main(String[] args) {
		System.out.println("This file contains basic info about Access modifiers.");
		/*
		Access modifiers: public, protected, default(lack of access modifiers), private
		
		public - this type of methods can be accessed anywhere regardless of file or package
		protected - this type of methods can be accessed anywhere in same package or we have to extend the package
		default - this type of methods can be accessed in the same class or package
		private - this type of methods can be accessed only in the same class 
		*/
	}
}
